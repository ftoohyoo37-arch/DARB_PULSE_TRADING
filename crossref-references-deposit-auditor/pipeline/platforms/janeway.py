def _scaffold(journal_dir: Path, ctx: dict) -> None:
    name = ctx["name"]
    slug = ctx.get("slug") or _basic_slug(name)
    batch_prefix = ctx.get("batch_prefix") or f"{slug}-issue"

    archive_url = ctx.get("archive_url", "").rstrip("/")
    parsed = urlparse(archive_url) if archive_url else None
    base_url = (
        f"{parsed.scheme}://{parsed.netloc}"
        if parsed and parsed.scheme else "https://example.org"
    )

    journal_dir.mkdir(parents=True, exist_ok=True)
    (journal_dir / "journal.py").write_text(
        _JOURNAL_PY.format(
            name=name, slug=slug, batch_prefix=batch_prefix,
            base_url=base_url,
        ),
        encoding="utf-8",
    )
    (journal_dir / "downloader.py").write_text(
        _DOWNLOADER_PY.format(name=name, slug=slug),
        encoding="utf-8",
    )
    (journal_dir / "scraper.py").write_text(
        _SCRAPER_PY.format(name=name),
        encoding="utf-8",
    )

    import json
    (journal_dir / "depositor.json").write_text(
        json.dumps({
            "depositor_name": ctx.get("depositor_name", ""),
            "email_address": ctx.get("depositor_email", ""),
        }, indent=2),
        encoding="utf-8",
    )

    # Janeway journals often have HTML galleys; prefer copying Prompt's
    # enricher (also HTML-aware). Fall back to ATD's.
    src_options = [
        journal_dir.parent / "Prompt" / "Structured Scraper" / "enricher.py",
        journal_dir.parent / "Across the Disciplines" / "Structured Scraper" / "enricher.py",
    ]
    dest_dir = journal_dir / "Structured Scraper"
    dest_dir.mkdir(parents=True, exist_ok=True)
    for src in src_options:
        if src.exists():
            (dest_dir / "enricher.py").write_bytes(src.read_bytes())
            req = src.parent / "requirements.txt"
            if req.exists():
                (dest_dir / "requirements.txt").write_bytes(req.read_bytes())
            break


PLATFORM = Platform(
    key="janeway",
    label="Janeway (Birkbeck / Open Library of Humanities)",
    detect=_detect,
    default_slug=_default_slug,
    scaffold=_scaffold,
    notes=[
        "Janeway issue subdirs are issue<id> (flat numeric, e.g. issue42), "
        "not vol/iss. The generated journal.py overrides VOLUME_DIR_PATTERN.",
        "Janeway articles usually expose both PDF and HTML galleys; the "
        "downloader saves both and the scraper prefers HTML when present.",
        "Detection relies on the Janeway generator meta tag or footer text; "
        "URL-only detection is intentionally weak to avoid false positives "
        "against custom CMS sites that happen to have /article/ paths.",
    ],
)
