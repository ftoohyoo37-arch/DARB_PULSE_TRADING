"""Janeway platform (open-source journal management system).

Janeway is Birkbeck / Open Library of Humanities' Django-based
alternative to OJS. Used by many OA humanities journals (Open Library
of Humanities titles, Modernist Cultures, Glossa-ish journals, etc.).

Detection signals:
  - HTML meta tag: <meta name="generator" content="Janeway ..." />
  - Footer text "powered by Janeway" or "Janeway Platform"
  - URL patterns specific to Janeway:
       /articles/                  → list of articles
       /article/id/<n>/            → article landing
       /article/<n>/galley/<gid>/download/
       /issues/                    → issue archive

URL conventions:
  - Issue archive: <base>/issues/
  - Issue page:    <base>/issue/<id>/
  - Article page:  <base>/article/id/<id>/
  - PDF download:  <base>/article/id/<id>/galley/<gid>/download/
  - DOIs are usually displayed prominently on the article page in the
    <meta name="citation_doi"> tag.
"""
from __future__ import annotations

import re
from pathlib import Path
from urllib.parse import urlparse

from .base import Platform, _basic_slug


_JANEWAY_PATH = re.compile(
    r"/(articles?|issues?|site)/(?:id/)?\d*/?$", re.IGNORECASE,
)


def _detect(url: str, html: str) -> bool:
    # Strongest signals: explicit branding (kept by community-edition deploys)
    if html:
        if re.search(r'<meta\s+name=["\']generator["\']\s+content=["\'][^"\']*Janeway',
                     html, re.IGNORECASE):
            return True
        if "powered by" in html.lower() and "janeway" in html.lower():
            return True
        if "Janeway Platform" in html:
            return True
        # URL-pattern detection for production deploys that have stripped
        # Janeway branding (e.g. OLH titles, Glossa, Modernist Cultures).
        # The /article/id/<n>/ form — with the literal "/id/" segment
        # between "article" and the numeric ID — is essentially unique
        # to Janeway. OJS uses /article/view/<n>, bepress uses
        # /<journal>/vol<N>/iss<M>/<art>/, WAC uses /docs/<journal>/...
        if re.search(r"/article/id/\d+/?", html):
            return True
        # Secondary URL fingerprints used by stripped-branding deploys.
        # Janeway distinctively uses /site/<name>/ for static pages
        # (about, contact, etc.) and /issue/<n>/ for issue landings.
        # OJS uses /about/, /issue/view/<n>; bepress uses /pages/<name>;
        # WAC has no equivalent. So /site/<name> + /issue/<n> together
        # is essentially a Janeway-only combination.
        has_issue_path = bool(re.search(r"/issue/\d+/?", html))
        has_site_path = bool(re.search(r"/site/[a-z][a-z0-9-]*/?", html))
        has_articles_path = "/articles/" in html
        if has_issue_path and (has_site_path or has_articles_path):
            return True
    return False


def _default_slug(url: str) -> tuple[str, str, str]:
    # Janeway journals usually have a host like <journal>.<publisher>.com,
    # so the subdomain is the best slug guess
    host = urlparse(url).netloc
    if "." in host:
        parts = host.split(".")
        if parts[0] not in ("www", ""):
            slug = parts[0].lower()
            name_guess = parts[0].replace("-", " ").title()
            return (name_guess, slug, f"{slug}-issue")
    return ("New Journal", "newjournal", "newjournal-issue")


