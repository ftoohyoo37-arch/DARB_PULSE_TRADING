"""Open Journal Systems platform (3.x).

OJS is the open-source journal management system used by hundreds of
small-press humanities journals. Detection: HTML meta tag
`<meta name="generator" content="Open Journal Systems...">` or URL
pattern `/index.php/<journal>/issue/`.

Article URLs:
    /index.php/<journal>/issue/archive
    /index.php/<journal>/issue/view/<id>
    /index.php/<journal>/article/view/<id>
    /index.php/<journal>/article/download/<id>/<galley_id>

OJS journals often expose both PDF and HTML galleys for each article.
The HTML galley is usually richer (inline DOIs, structured reference
list), so the downloader saves both and the scraper prefers HTML when
it's available.
"""
from __future__ import annotations

import re
from pathlib import Path
from urllib.parse import urlparse

from .base import Platform, _basic_slug


_OJS_PATH = re.compile(r"/index\.php/([a-z0-9_-]+)/", re.IGNORECASE)


def _detect(url: str, html: str) -> bool:
    if _OJS_PATH.search(url):
        return True
    if html:
        if "Open Journal Systems" in html:
            return True
        if 'name="generator"' in html and "OJS" in html:
            return True
    return False


def _default_slug(url: str) -> tuple[str, str, str]:
    m = _OJS_PATH.search(url)
    slug = m.group(1).lower() if m else "newjournal"
    name_guess = slug.replace("-", " ").replace("_", " ").title()
    return (name_guess, slug, f"{slug}-issue")


