def _scaffold(journal_dir: Path, ctx: dict) -> None:
    """Write the per-journal scaffolding files into `journal_dir`.

    ctx keys consumed:
      name, slug, batch_prefix, base_url, archive_url,
      force_ocr_volumes (list[int]),
      depositor_name, depositor_email
    """
    name = ctx["name"]
    slug = ctx.get("slug") or _basic_slug(name)
    batch_prefix = ctx.get("batch_prefix") or f"{slug}-volume"
    base_url = ctx.get("base_url") or "https://wacclearinghouse.org"
    # WAC Clearinghouse volume URL conventions vary; user can edit later.
    archive_url = ctx.get("archive_url", "").rstrip("/")
    if "{n}" in archive_url:
        volume_url_template = archive_url
    elif "/volume" in archive_url.lower():
        volume_url_template = re.sub(r"/volume\d+/?$", "/volume{n}/", archive_url) + "/"
        volume_url_template = volume_url_template.rstrip("/") + "/"
    else:
        volume_url_template = f"{base_url}/{slug}/archives/volume{{n}}/"

    force_ocr_volumes = ctx.get("force_ocr_volumes") or []

    journal_dir.mkdir(parents=True, exist_ok=True)
    (journal_dir / "journal.py").write_text(
        _JOURNAL_PY.format(
            name=name, slug=slug, batch_prefix=batch_prefix,
            force_ocr_volumes=repr(list(force_ocr_volumes)),
        ),
        encoding="utf-8",
    )
    (journal_dir / "downloader.py").write_text(
        _DOWNLOADER_PY.format(
            name=name, slug=slug,
            base_url=base_url,
            volume_url_template=volume_url_template,
        ),
        encoding="utf-8",
    )
    (journal_dir / "scraper.py").write_text(
        _SCRAPER_PY.format(name=name),
        encoding="utf-8",
    )

    # depositor.json (gitignored — credentials)
    import json
    (journal_dir / "depositor.json").write_text(
        json.dumps({
            "depositor_name": ctx.get("depositor_name", ""),
            "email_address": ctx.get("depositor_email", ""),
        }, indent=2),
        encoding="utf-8",
    )

    # Stub the Structured Scraper enricher dir; copy the closest existing
    # enricher.py from a sibling WAC journal as the starter, since the
    # enrichers are nearly identical across journals.
    _stub_enricher(journal_dir)


def _stub_enricher(journal_dir: Path) -> None:
    """Copy an existing WAC journal's enricher.py as the starter."""
    src = journal_dir.parent / "Across the Disciplines" / "Structured Scraper" / "enricher.py"
    dest_dir = journal_dir / "Structured Scraper"
    dest_dir.mkdir(parents=True, exist_ok=True)
    if src.exists():
        (dest_dir / "enricher.py").write_bytes(src.read_bytes())
        req = src.parent / "requirements.txt"
        if req.exists():
            (dest_dir / "requirements.txt").write_bytes(req.read_bytes())
    else:
        (dest_dir / "enricher.py").write_text(
            "# TODO: copy enricher.py from a sibling journal\n",
            encoding="utf-8",
        )


