"""WAC Clearinghouse platform (wac.colostate.edu / wacclearinghouse.org).

Hosts: ATD, The WAC Journal, JBW, Double Helix, JWA, and many others
in writing studies. Volume URLs follow:
    https://wac.colostate.edu/docs/<slug>/vol<N>/<author>.pdf
    https://wacclearinghouse.org/<slug>/archives/volume<N>/

The downloader walks the volume index page (HTML), pulls every <a href>
ending in `.pdf` whose URL contains `/docs/<slug>/vol<N>/`, and downloads
each into `pdfs/volume<N>/`.
"""
from __future__ import annotations

import re
from pathlib import Path
from urllib.parse import urlparse

from .base import Platform, _basic_slug


def _detect(url: str, html: str) -> bool:
    host = urlparse(url).netloc.lower()
    if "wac.colostate.edu" in host:
        return True
    if "wacclearinghouse.org" in host:
        return True
    # Fallback: HTML body contains a WAC Clearinghouse footer
    if "WAC Clearinghouse" in (html or ""):
        return True
    return False


_SLUG_FROM_URL = re.compile(
    r"(?:wacclearinghouse\.org|wac\.colostate\.edu)/(?:docs/)?([a-z0-9_-]+)/",
    re.IGNORECASE,
)


def _default_slug(url: str) -> tuple[str, str, str]:
    """Guess (name, slug, batch_prefix). Best-effort from URL path."""
    m = _SLUG_FROM_URL.search(url)
    if m:
        slug = m.group(1).lower()
        # Title-case the slug as a name guess
        name_guess = slug.replace("-", " ").replace("_", " ").upper()
        return (name_guess, slug, f"{slug}-volume")
    return ("New Journal", "newjournal", "newjournal-volume")


