def _scaffold(journal_dir: Path, ctx: dict) -> None:
    name = ctx["name"]
    slug = ctx.get("slug") or _basic_slug(name)
    batch_prefix = ctx.get("batch_prefix") or f"{slug}-issue"
    archive_url = ctx.get("archive_url", "").rstrip("/")

    # Extract base + journal path from the archive URL
    parsed = urlparse(archive_url) if archive_url else None
    base_url = f"{parsed.scheme}://{parsed.netloc}" if parsed and parsed.scheme else "https://example.org"
    path = parsed.path if parsed else "/"
    # Trim trailing all_issues.html or pages so journal_path ends with /
    path = re.sub(r"/all_issues\.html$", "/", path, flags=re.IGNORECASE)
    if not path.endswith("/"):
        path += "/"
    journal_path = path or f"/{slug}/"

    force_ocr_volumes = ctx.get("force_ocr_volumes") or []

    journal_dir.mkdir(parents=True, exist_ok=True)
    (journal_dir / "journal.py").write_text(
        _JOURNAL_PY.format(
            name=name, slug=slug, batch_prefix=batch_prefix,
            force_ocr_volumes=repr(list(force_ocr_volumes)),
        ),
        encoding="utf-8",
    )
    (journal_dir / "downloader.py").write_text(
        _DOWNLOADER_PY.format(
            name=name, slug=slug,
            base_url=base_url, journal_path=journal_path,
        ),
        encoding="utf-8",
    )
    (journal_dir / "scraper.py").write_text(
        _SCRAPER_PY.format(name=name),
        encoding="utf-8",
    )

    import json
    (journal_dir / "depositor.json").write_text(
        json.dumps({
            "depositor_name": ctx.get("depositor_name", ""),
            "email_address": ctx.get("depositor_email", ""),
        }, indent=2),
        encoding="utf-8",
    )

    # Copy ATD's enricher.py as a starter (it's CrossRef-only and works
    # for any journal type)
    src = journal_dir.parent / "Across the Disciplines" / "Structured Scraper" / "enricher.py"
    dest_dir = journal_dir / "Structured Scraper"
    dest_dir.mkdir(parents=True, exist_ok=True)
    if src.exists():
        (dest_dir / "enricher.py").write_bytes(src.read_bytes())
        req = src.parent / "requirements.txt"
        if req.exists():
            (dest_dir / "requirements.txt").write_bytes(req.read_bytes())


PLATFORM = Platform(
    key="bepress",
    label="bepress / Digital Commons",
    detect=_detect,
    default_slug=_default_slug,
    scaffold=_scaffold,
    notes=[
        "bepress journals use vol<N>iss<M> subdirs (e.g. pdfs/vol3iss1/), "
        "not WAC-style volumeN. The generated journal.py overrides "
        "VOLUME_DIR_PATTERN to match.",
        "The downloader scrapes <archive>/all_issues.html for issue links, "
        "then each article page for the `bepress_citation_pdf_url` meta tag. "
        "Both signals are stable across the bepress platform.",
        "Many bepress sites front a single PDF download URL that requires "
        "session cookies (`cgi/viewcontent.cgi`). The downloader uses a "
        "persistent requests.Session so this usually just works.",
    ],
)
