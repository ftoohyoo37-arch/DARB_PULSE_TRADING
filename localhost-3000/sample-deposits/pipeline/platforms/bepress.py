"""bepress / Digital Commons platform (Elsevier's institutional repository stack).

Hosts a huge fraction of US institutional repositories — any URL like
`https://digitalcommons.<institution>.edu/<journal>/` or
`https://scholar.<institution>.edu/<journal>/` is overwhelmingly likely
to be bepress. Detection is multi-pronged:

  - URL host matches `digitalcommons.*.edu` or `scholarship.*.edu` /
    `scholarworks.*.edu` etc.
  - HTML body contains `<meta name="bepress_citation_*">` tags (every
    article page emits these, and many archive pages do too).
  - "powered by bepress" / "Digital Commons" footer text.

URL conventions:
  - Archive listing: <base>/<journal>/all_issues.html (lists every vol/iss)
  - Issue page:      <base>/<journal>/vol<N>/iss<M>/
  - Article page:    <base>/<journal>/vol<N>/iss<M>/<art_num>/
  - PDF download:    <base>/cgi/viewcontent.cgi?article=<id>&context=<journal>
                     OR a direct .pdf URL listed in `bepress_citation_pdf_url`
                     meta tag on the article page.

The generated downloader walks all_issues.html, follows each issue
link, scrapes article PDF URLs from the `bepress_citation_pdf_url`
meta tag on each article page, and downloads to pdfs/vol<N>iss<M>/.
"""
from __future__ import annotations

import re
from pathlib import Path
from urllib.parse import urlparse

from .base import Platform, _basic_slug


_BEPRESS_HOST = re.compile(
    r"^(digitalcommons|scholarworks|scholarship|scholar|repository|commons|"
    r"openscholarship|ir|works|via|trace|engagedscholarship|scholarcommons)"
    r"\.[a-z0-9-]+\.(edu|org)$",
    re.IGNORECASE,
)


def _detect(url: str, html: str) -> bool:
    host = urlparse(url).netloc.lower()
    if _BEPRESS_HOST.match(host):
        return True
    if html:
        if 'name="bepress_citation_' in html:
            return True
        if "Powered by" in html and "bepress" in html.lower():
            return True
        if "Digital Commons" in html and "bepress" in html.lower():
            return True
    return False


_JOURNAL_FROM_URL = re.compile(
    r"^https?://[^/]+/([a-z0-9_-]+)(?:/|$)", re.IGNORECASE,
)


def _default_slug(url: str) -> tuple[str, str, str]:
    m = _JOURNAL_FROM_URL.match(url)
    if m and m.group(1).lower() not in ("cgi", "do", "vol", "static"):
        slug = m.group(1).lower()
        name_guess = slug.replace("-", " ").replace("_", " ").title()
        return (name_guess, slug, f"{slug}-issue")
    return ("New Journal", "newjournal", "newjournal-issue")


